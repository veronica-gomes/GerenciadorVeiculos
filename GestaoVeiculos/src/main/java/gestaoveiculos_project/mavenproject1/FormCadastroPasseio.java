    public boolean placaExiste(String placa) {
        return bdVeiculos.getListaPasseio().stream().anyMatch(e -> e.getPlaca().equalsIgnoreCase(placa))
                || bdVeiculos.getListaCarga().stream().anyMatch(e -> e.getPlaca().equalsIgnoreCase(placa));
    }

