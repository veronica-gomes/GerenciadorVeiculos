    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            String placa = fieldPlaca.getText().trim();
            if (placaExiste(placa)) {
                JOptionPane.showMessageDialog(this, "Placa já cadastrada!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            passeio = new Passeio();
            passeio.setPlaca(placa);
            passeio.setMarca(fieldMarca.getText().trim());
            passeio.setModelo(fieldModelo.getText().trim());
            passeio.setCor(fieldCor.getText().trim());

            try {
                float vel = Float.parseFloat(fieldVeloc.getText().trim());
                if (vel < 80 || vel > 110) {
                    passeio.setVelocMax(100);
                    throw new VelocException();
                } else {
                    passeio.setVelocMax(vel);
                }
            } catch (VelocException ve) {
                JOptionPane.showMessageDialog(this, "A velocidade está fora dos limites brasileiros.\nVelocidade ajustada para 100 km/h.\n NOME DA CLASSE: " + ve.getClass().getName());
            } catch (NumberFormatException ne) {
                JOptionPane.showMessageDialog(this, "Velocidade inválida.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Resto dos campos
            passeio.setQtdRodas(Integer.parseInt(fieldRodas.getText().trim()));
            passeio.setQtdPassageiros(Integer.parseInt(fieldQtd.getText().trim()));
            passeio.getMotor().setQtdPist(Integer.parseInt(fieldPistoes.getText().trim()));
            passeio.getMotor().setPotencia(Integer.parseInt(fieldPotenc.getText().trim()));

            passeio.calcVel(passeio.getVelocMax());
            passeio.calcular();

            bdVeiculos.getListaPasseio().add(passeio);

            JOptionPane.showMessageDialog(this, "Veículo cadastrado com sucesso!");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Há campos do tipo numerico que não estão preenchidos corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro não tratado: " + ex.getMessage() + Arrays.toString(ex.getStackTrace()), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

